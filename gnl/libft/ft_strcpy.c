#include "libft.h"

char	*ft_strcpy(char *dest, char *src)
{
	unsigned int		i;

	i = 0;
	if (src == NULL)
		return (dest);
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = src[i];
	return (dest);
}
